        <h1>GATE-2020: Introduction</h1>
        <img class="imgr borderedbox" src="../images/120x120.gif" alt="">
       <p>Graduate Aptitude Test in Engineering (GATE) is an examination conducted jointly by the Indian Institute of Science (IISc), Bangalore and the seven Indian Institutes of Technology (at Bombay, Delhi, Guwahati, Kanpur, Kharagpur, Madras and Roorkee) on behalf of the National Coordination Board (NCB)-GATE, Department of Higher Education, Ministry of Human Resource Development (MHRD), Government of India.</p>
	   <p>
	   Qualifying in GATE is a mandatory requirement for seeking admission and/or financial assistance to (i) Master’s programs and direct Doctoral programs in Engineering/Technology/Architecture and (ii) Doctoral programs in relevant branches of Science, in the institutions supported by the MHRD and other Government agencies. Even in some colleges and institutions, which admit students without MHRD scholarship/assistantship, the GATE qualification is mandatory. Further, many Public Sector Undertakings (PSUs) have been using the GATE score in their recruitment process.</p>
	   <p>The information in this brochure is mainly categorized into Pre-Examination (Eligibility, Application Submission, Examination Centres, etc.), Examination (Pattern, Marks/Score, etc.) and Post-Examination (Answers, Contests, GATE 2020 Results, Scorecard, etc.) sections.</p>
	   <section id="about">
<h1>About GATE-2020</h1>
<p>Graduate Aptitude Test in Engineering (GATE) is basically an examination on the comprehensive understanding of the candidates in various undergraduate subjects in Engineering/Technology/Architecture and post-graduate level subjects in Science. GATE 2020 will be conducted for 24 subjects (also referred to as “papers”) and it would be distributed over 2nd, 3rd, 9th  & 10th  of February 2020. The GATE examination centers are spread in different cities across India, as well as, in six cities abroad. The examination would be purely a Computer Based Test (CBT).</p>
<p>The GATE score would reflect the relative performance level of the candidate in a particular subject, which is quantified based on the several years of examination data. Note that the GATE 2020 score is valid for THREE YEARS from the date of announcement of the GATE 2020 Results.</p>
</section>
<section id="eligible">
<h1>The following categories of candidates are eligible to appear for GATE :</h1>
<p>Bachelor's degree holders in Engineering/Technology/ Architecture (4 years after 10+2) and those who are in the final year of such programmes.</p>
<p>Bachelor's degree holders in Engineering/Technology/Architecture (Post-B.Sc./Post-Diploma) and those who are in the final year of such programmes.</p>
<p>Master's degree holders in any branch of Science/Mathematics/ Statistics/Computer Applications or equivalent and those who are in the final year of such programmes.</p>
<p>Candidates in the second or higher year of the Four-year Integrated Master's degree programmer (Post-B.Sc.) in Engineering/Technology.</p>
<p>Candidates in the fourth or higher year of Five-year Integrated Master's degree programme or Dual Degree programme in Engineering/Technology.</p>
<p>Candidates with qualifications obtained through examinations conducted by professional societies recognized by UPSC/AICTE (e.g. AMIE by Institute of Engineers (India); AMICE by Institute of Civil Engineers (India)) as equivalent to B.E./B.Tech. Those who have completed section A or equivalent of such professional courses are also eligible.
</p>
</section>
<h1>Administration</h1>
<p>GATE is administered jointly by the Indian Institute of Science (IISc), Bangalore and seven Indian Institutes of Technology (namely, IITs at Bombay, Delhi, Guwahati, Kanpur, Kharagpur, Madras, and Roorkee). The GATE Committee, which comprises of representatives from the administering institutes, is the sole authority for regulating the examination and for declaring the Gate 2020 results. For administrative purposes, the examination cities in India are segregated into eight zones. The operations related to GATE, in each of the eight zones, are managed by a Zonal GATE Office at the administering institute (IIT or IISc).</p>
<h1>Scholarship/Assistantship for Postgraduate Programs</h1>
<p>To avail the financial assistance (scholarship/assistantship), the candidate must first secure the admission to a program in one of the central government supported institutes, by a procedure that could vary from institute to institute. Depending upon the norms adopted by a specific institute or department of the institute, a candidate may be admitted directly into a course based on his/her performance in GATE only; or based on his/her performance in GATE and an admission test/interview conducted by the department to which he/she has applied and/or the candidate’s academic record. In the test/interview based selection procedure, as per the MHRD guidelines, a minimum of 70% weightage will be given to the performance in and the remaining will be GATE given to the candidate’s performance in test/interview and/or academic record. However, the admitting institutes could prescribe a minimum passing marks in the test/interview.
</p>
<p>Candidates are advised to seek the complete details of the admission procedures and the availability of MHRD scholarship/assistantship from the corresponding admitting institutions. The criteria for postgraduate admission with scholarship/assistantship could be different for different institutions. The management of the post-graduate scholarship/assistantship is also the responsibility of the admitting institution. Similarly, reservation of the seats for different categories will be as per the policies and norms of the admitting institution and the Government of India rules.
</p>
<p> 
The admitting institute may also specify the number of candidates who will be provided financial assistance (scholarship) if admission is secured. Qualification in GATE is also a minimum requirement to apply for various fellowships awarded by many Government organizations.
</p>
 
<b>
<p>Note:</p>
<p>1.Call for admissions to M.Tech./Ph.D. will be advertised separately by the respective institutions and GATE committee is not responsible for the admission process.
</p><p>2.GATE zonal offices will not entertain any inquiry about admission, reservation of seats and/or award of scholarship/assistantship.
</p></b>
<h1>Use of GATE score for Employment</h1>
<p>In the past, several Public Sector Undertakings (PSUs) have used GATE scores to shortlist the candidates for employment. A few such organizations are: Bharat Heavy Electricals Limited (BHEL), Gas Authority of India Limited (GAIL), Hindustan Aeronautics Limited (HAL), Indian Oil Corporation Limited (IOCL), National Thermal Power Corporation (NTPC), Nuclear Power Corporation of India Limited (NPCIL), Oil and Natural Gas Corporation (ONGC) and Power Grid Corporation of India (PGCI).</p>
<p>Direct recruitment to Group A level posts in Central government, i.e., Senior Field Officer (Tele), Senior Research Officer (Crypto) and Senior Research Officer (S&T) in Cabinet Secretariat, Government of India, is now being carried out on the basis of GATE score. The details of the scheme of recruitment are normally published in National Newspapers/Employment News/Rozgar Samachar by the concerned authority.</p>
<p>
Some other Governments of India Organizations have also expressed their interest to utilize GATE 2020 score for their recruitment purpose.
</p><b><p>
Note: GATE administration is not responsible for employment opportunities. The qualified candidates must check newspapers and other reliable sources for any such information.</p>
</b> 
<section id="exam pattern"><h1>Exam Structure</h1>
<p>The GATE examination for the papers AE, AG, AR, BT, CE, CH, CS,CY, EC, EE, EY, GG, IN,MA, ME,MN, MT, PH, PI, TF, XE and XL will be conducted ONLINE using computers .In all the papers, there will be a total of 65 questions carrying 100 marks, out of which 10 questions carrying a total of 15 marks are in General Aptitude (GA).
</p>
<p>In the papers bearing the codes AE, AG, BT, CE, CH, CS, EC, EE, IN, ME, MN, MT, PI, TF and XE, the Engineering Mathematics will carry around 15% of the total marks, the General Aptitude section will carry 15% of the total marks and the remaining percentage of the total marks is devoted to the subject of the paper.
</p>
<p>In the papers bearing the codes AR, CY, EY, GG, MA, PH and XL, the General Aptitude section will carry 15% of the total marks and the remaining 85% of the total marks is devoted to the subject of the paper.
</p>
<h1>GATE paper contain questions of two different types:</h1>
<p>
(i) Multiple Choice Questions (MCQ) carrying 1 or 2 marks each in all papers and sections. These questions are objective in nature, and each will have a choice of four answers, out of which the candidate has to mark the correct answer(s).</p>
<p>
(ii) Numerical Answer Questions of 1 or 2 marks each in all papers and sections. For these questions the answer is a real number, to be entered by the candidate using the virtual keypad. No choices will be shown for this type of questions.</p>


<h1>The questions in a paper may be designed to test the following abilities:</h1>
<p>
(i)Recall: These are based on facts, principles, formulae or laws of the discipline of the paper. The candidate is expected to be able to obtain the answer either from his/her memory of the subject or at most from a one-line computation.</p>
<p>(ii) Comprehension: These questions will test the candidate’s understanding of the basics of his/her field, by requiring him/her to draw simple conclusions from fundamental ideas.</p>
<p>(iii) Application: In these questions, the candidate is expected to apply his/her knowledge either through computation or by logical reasoning.</p>
<p>(iv) Analysis and Synthesis: In these questions, the candidate is presented with data, diagrams, images etc. that require analysis before a question can be answered. A Synthesis question might require the candidate to compare two or more pieces of information. Questions in this category could, for example, involve candidates in recognizing unstated assumptions, or separating useful information from irrelevant information.</p>
</section>
